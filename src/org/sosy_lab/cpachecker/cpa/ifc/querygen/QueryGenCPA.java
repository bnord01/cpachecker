@Options(prefix="querygen",description = "Analysis that collects possible queries for ifc analysis")
public class QueryGenCPA implements ConfigurableProgramAnalysis {

  public static CPAFactory factory() {
    CPAFactory fac = AutomaticCPAFactory.forType(QueryGenCPA.class);
    return fac;
  }

  private CFA cfa;
  private AbstractDomain abstractDomain;
  private TransferRelation transferRelation;
  private MergeOperator mergeOperator;
  private StopOperator stopOperator;

  private QueryGenCPA(Configuration config, CFA pCFA, LogManager logger) throws InvalidConfigurationException {
    config.inject(this);

    this.cfa = pCFA;

    this.abstractDomain = DelegateAbstractDomain.<QueryGenState>getInstance();

    this.transferRelation = new QueryGenTransferRelation();

    this.mergeOperator = new QueryGenMergeOperator(new MergeJoinOperator(abstractDomain),logger,config);

    this.stopOperator = new StopSepOperator(abstractDomain);
  }

  @Override
  public AbstractDomain getAbstractDomain() {
    return abstractDomain;
  }

  @Override
  public TransferRelation getTransferRelation() {
    return transferRelation;
  }

  @Override
  public MergeOperator getMergeOperator() {
    return mergeOperator;
  }

  @Override
  public StopOperator getStopOperator() {
    return stopOperator;
  }

  @Override
  public PrecisionAdjustment getPrecisionAdjustment() {
    return StaticPrecisionAdjustment.getInstance();
  }


  @Override
  public AbstractState getInitialState(CFANode pNode, StateSpacePartition pPartition) {
    return new QueryGenState(Sets.<CFANode>newHashSet(cfa.getAllFunctionHeads()),Sets.<Pair<CFANode,Variable>>newHashSet());
  }

  @Override
  public Precision getInitialPrecision(CFANode pNode, StateSpacePartition pPartition) {
    return SingletonPrecision.getInstance();
  }
}
